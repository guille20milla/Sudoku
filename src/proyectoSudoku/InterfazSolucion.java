    class MyTableModel extends AbstractTableModel {

        Integer[][] data = null;

        /**
         * Constructor del modelo
         */
        public MyTableModel() {
            data = rellenarTabla();
        }

        /**
         * Metodo que rellena la tabla
         * @return data
         */
        public Integer[][] rellenarTabla() {

            int numFilas = 9;
            int numColumns = getColumnCount();
            Integer[][] data = new Integer[numFilas][numColumns];
            for (int fila = 0; fila < numFilas; fila++) {
                for (int columna = 0; columna < numColumns; columna++) {
                    data[fila][columna] = sudoku[fila][columna].getValor();
                }
            }

            return data;
        }

        /**
         * Cuenta las columnas
         *
         * @return numero de columnas
         */
        public int getColumnCount() {
            return 9;
        }

        /**
         * Cuenta las filas
         *
         * @return numero de filas
         */
        public int getRowCount() {
            return data.length;
        }

        /**
         * Metodo que devuelve el nombre de la columna
         *
         * @param col numero de la columna
         * @return nombre de la columna
         */
        public String getColumnName(int col) {
            return "";
        }

        /**
         * Metodo que devuelve los datos de la tabla
         *
         * @param row numero fila
         * @param col numero columna
         * @return datos de la tabla
         */
        public Object getValueAt(int row, int col) {
            return data[row][col];
        }

    }

