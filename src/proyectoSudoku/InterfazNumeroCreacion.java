    /**
     * Metodo que comprueba si se ha intrucido bien los datos
     * @param num
     * @return 
     */
    public boolean numeroValido(String num) {
        Pattern p = Pattern.compile("\\d");
        Matcher m = p.matcher(num);
        if(m.matches()){
            return true;
        }else{
            return false;
        }
        
    }


